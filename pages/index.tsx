import Head from 'next/head';
import { gql } from '@apollo/client';
import Layout from '@/components/Layout/Layout';
import client from '@/utilities/ApolloClientConnection/ApolloClientConnection';
import TopInfoSection from '@/components/TopInfoSection/TopInfoSection';
import PinnedRepo from '@/components/PinnedRepos/PinnedRepo';
import { useEffect, useState } from 'react';
import { useAuth } from '@/contexts/AuthProvider';
import { useRouter } from 'next/router';
import Loading from '@/components/Loading/Loading';

interface IPROPS {
  data: {
    pinnedItems: {
      totalCount: number;
    };
    repositories: {
      totalCount: number;
      totalDiskUsage: number;
    };
    watching: {
      totalCount: number;
    };

    avatarUrl: string;
    login: string;
    name: string;
  };
}

const Home: React.FC<IPROPS> = ({ data }) => {
  const router = useRouter();
  const { user, loading } = useAuth();

  useEffect(() => {
    if (loading || user) {
      console.log('logged In');
    } else {
      router.push('/login');
    }
  }, [user, loading, router]);

  if (loading) {
    return <Loading />;
  }
  if (!user) {
    return null;
  }

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <main>
          <TopInfoSection data={data} />
          <PinnedRepo />
        </main>
      </Layout>
    </>
  );
};

export async function getStaticProps() {
  const { data } = await client.query({
    query: gql`
      {
        user(login: "tausifabid12") {
          pinnedItems {
            totalCount
          }
          repositories {
            totalCount
            totalDiskUsage
          }
          watching {
            totalCount
          }
          avatarUrl(size: 500)
          login
          name
        }
      }
    `,
  });

  const { user } = data;

  return {
    props: { data: user },
  };
}

export default Home;
